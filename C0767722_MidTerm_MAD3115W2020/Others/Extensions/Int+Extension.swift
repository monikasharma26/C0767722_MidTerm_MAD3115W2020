import Foundation
extension Int {
    
    var billWithGB: String {
        return "\(self) GB"
    }
    
    var billWithMinutes: String {
        return "\(self) Minutes"
    }
    
    var billWithUnits: String {
        return "\(self) Units"
    }
}
